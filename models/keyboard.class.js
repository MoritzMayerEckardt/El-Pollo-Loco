/**
 * Represents keyboard inputs.
 */
class Keyboard {
    /**
     * Represents the left arrow key.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Represents the right arrow key.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Represents the up arrow key.
     * @type {boolean}
     */
    UP = false;

    /**
     * Represents the down arrow key.
     * @type {boolean}
     */
    DOWN = false;

    /**
     * Represents the space key.
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Represents the D key.
     * @type {boolean}
     */
    D = false;

    /**
     * Represents the W key.
     * @type {boolean}
     */
    W = false;

    /**
     * Represents the X key.
     * @type {boolean}
     */
    X = false;

    /**
     * Represents the Y key.
     * @type {boolean}
     */
    Y = false;

    /**
     * Represents the ESC key.
     * @type {boolean}
     */
    ESC = false;
}
